#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Compute binary classification metrics (acc / precision / recall) for:
- ikun: filtered tracking results in `predict.txt`
- refermllm: tracking results with confidence in `predict_with_conf.txt` (needs threshold)

Positive set comes from `outputs/refer_kitti_motc_gt/<seq>__<query>/gt/gt.txt`.
All other candidate objects (defined by MOT candidate list) are negatives.

Candidate universe MUST match `refer_llm/llm_eval.py`:
we use `runs/**/probs/mot/<seq>.txt` (generated by llm_eval multi-threshold eval),
each line is: frame_id,obj_id,x,y,w,h,1.0

This script is intentionally self-contained and does NOT require KITTI dataset files.
"""

from __future__ import annotations

import argparse
import json
import os
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, Iterable, List, Optional, Sequence, Set, Tuple


SampleKey = Tuple[int, int]  # (frame_idx, obj_id)


@dataclass
class Confusion:
    tp: int = 0
    fp: int = 0
    tn: int = 0
    fn: int = 0

    @property
    def total(self) -> int:
        return self.tp + self.fp + self.tn + self.fn

    def add(self, other: "Confusion") -> None:
        self.tp += int(other.tp)
        self.fp += int(other.fp)
        self.tn += int(other.tn)
        self.fn += int(other.fn)

    def metrics(self) -> Dict[str, float]:
        total = self.total
        acc = float(self.tp + self.tn) / float(total) if total > 0 else 0.0
        prec_den = self.tp + self.fp
        rec_den = self.tp + self.fn
        precision = float(self.tp) / float(prec_den) if prec_den > 0 else 0.0
        recall = float(self.tp) / float(rec_den) if rec_den > 0 else 0.0
        return {
            "acc": acc,
            "precision": precision,
            "recall": recall,
            "tp": float(self.tp),
            "fp": float(self.fp),
            "tn": float(self.tn),
            "fn": float(self.fn),
            "total": float(total),
        }


def _safe_int(x: str) -> int:
    # Handles "34", "34.0"
    return int(float(x))


def _read_lines(path: Path) -> List[str]:
    if not path.is_file():
        return []
    try:
        return [ln.strip() for ln in path.read_text(encoding="utf-8").splitlines() if ln.strip()]
    except UnicodeDecodeError:
        return [ln.strip() for ln in path.read_text(encoding="latin-1").splitlines() if ln.strip()]


def find_latest_mot_dir(runs_root: Path) -> Optional[Path]:
    """
    Find newest directory matching runs/**/probs/mot containing at least one *.txt.
    We choose by latest mtime among contained files.
    """
    if not runs_root.is_dir():
        return None
    best_dir: Optional[Path] = None
    best_mtime: float = -1.0
    for mot_dir in runs_root.rglob("probs/mot"):
        if not mot_dir.is_dir():
            continue
        # any txt?
        txts = list(mot_dir.glob("*.txt"))
        if not txts:
            continue
        try:
            mtime = max(p.stat().st_mtime for p in txts)
        except Exception:
            continue
        if mtime > best_mtime:
            best_mtime = mtime
            best_dir = mot_dir
    return best_dir


def find_best_mot_dir(runs_root: Path, required_seqs: Optional[Set[str]] = None) -> Optional[Path]:
    """
    Choose a mot_dir under runs/**/probs/mot.
    - If required_seqs is provided: prefer dirs that contain ALL required seq files.
      Otherwise pick the one with the highest coverage; tie-break by latest mtime.
    - If required_seqs is None: fallback to latest by mtime (same as find_latest_mot_dir).
    """
    if required_seqs is None or len(required_seqs) == 0:
        return find_latest_mot_dir(runs_root)
    if not runs_root.is_dir():
        return None

    best_dir: Optional[Path] = None
    best_cover: int = -1
    best_full: bool = False
    best_mtime: float = -1.0

    for mot_dir in runs_root.rglob("probs/mot"):
        if not mot_dir.is_dir():
            continue
        txts = list(mot_dir.glob("*.txt"))
        if not txts:
            continue
        avail = {p.stem for p in txts}
        cover = len(required_seqs.intersection(avail))
        full = cover == len(required_seqs)
        try:
            mtime = max(p.stat().st_mtime for p in txts)
        except Exception:
            mtime = 0.0

        # Ranking:
        # 1) full coverage beats non-full
        # 2) higher coverage beats lower
        # 3) newer mtime beats older
        better = False
        if full and not best_full:
            better = True
        elif full == best_full and cover > best_cover:
            better = True
        elif full == best_full and cover == best_cover and mtime > best_mtime:
            better = True

        if better:
            best_dir = mot_dir
            best_cover = cover
            best_full = full
            best_mtime = mtime

    return best_dir


def parse_mot_candidates(mot_file: Path) -> List[SampleKey]:
    """
    mot_file format: fidx,oid,x,y,w,h,score
    """
    keys: List[SampleKey] = []
    for ln in _read_lines(mot_file):
        parts = [p.strip() for p in ln.split(",")]
        if len(parts) < 2:
            continue
        fidx = _safe_int(parts[0])
        oid = _safe_int(parts[1])
        keys.append((fidx, oid))
    return keys


def parse_gt_positive(gt_file: Path) -> Dict[int, Set[int]]:
    """
    gt/gt.txt format (TrackEval-like):
      frame, id, x, y, w, h, ...
    We only need per-frame positive object ids.
    """
    pos: Dict[int, Set[int]] = {}
    for ln in _read_lines(gt_file):
        parts = [p.strip() for p in ln.split(",")]
        if len(parts) < 2:
            continue
        fidx = _safe_int(parts[0])
        oid = _safe_int(parts[1])
        pos.setdefault(fidx, set()).add(oid)
    return pos


def parse_ikun_predict(predict_file: Path) -> Set[SampleKey]:
    """
    ikun predict.txt example:
      frame,id,x,y,w,h,1,-1,-1,-1
    We treat presence as y_pred=1.
    """
    pred: Set[SampleKey] = set()
    for ln in _read_lines(predict_file):
        parts = [p.strip() for p in ln.split(",")]
        if len(parts) < 2:
            continue
        fidx = _safe_int(parts[0])
        oid = _safe_int(parts[1])
        pred.add((fidx, oid))
    return pred


def parse_refermllm_predict_with_conf(predict_file: Path) -> Dict[SampleKey, float]:
    """
    refermllm predict_with_conf.txt example:
      frame,id,x,y,w,h,conf
    We keep max conf for each (frame,id).
    """
    conf: Dict[SampleKey, float] = {}
    for ln in _read_lines(predict_file):
        parts = [p.strip() for p in ln.split(",")]
        if len(parts) < 7:
            continue
        fidx = _safe_int(parts[0])
        oid = _safe_int(parts[1])
        try:
            c = float(parts[6])
        except Exception:
            continue
        k = (fidx, oid)
        prev = conf.get(k)
        conf[k] = c if prev is None else max(prev, c)
    return conf


def compute_confusion(
    candidates: Iterable[SampleKey],
    pos_by_frame: Dict[int, Set[int]],
    ikun_pred: Optional[Set[SampleKey]] = None,
    refer_conf: Optional[Dict[SampleKey, float]] = None,
    refer_threshold: float = 0.5,
) -> Tuple[Confusion, Confusion]:
    """
    Returns (ikun_confusion, refermllm_confusion)
    If a prediction source is None, it behaves as "predict nothing" (all negatives).
    """
    ikun = Confusion()
    refer = Confusion()
    ikun_pred = ikun_pred or set()
    refer_conf = refer_conf or {}

    for (fidx, oid) in candidates:
        y_true = 1 if oid in pos_by_frame.get(int(fidx), set()) else 0

        # ikun
        y_pred_i = 1 if (fidx, oid) in ikun_pred else 0
        if y_true == 1 and y_pred_i == 1:
            ikun.tp += 1
        elif y_true == 0 and y_pred_i == 1:
            ikun.fp += 1
        elif y_true == 0 and y_pred_i == 0:
            ikun.tn += 1
        else:
            ikun.fn += 1

        # refermllm
        p = float(refer_conf.get((fidx, oid), 0.0))
        y_pred_r = 1 if p >= float(refer_threshold) else 0
        if y_true == 1 and y_pred_r == 1:
            refer.tp += 1
        elif y_true == 0 and y_pred_r == 1:
            refer.fp += 1
        elif y_true == 0 and y_pred_r == 0:
            refer.tn += 1
        else:
            refer.fn += 1

    return ikun, refer


def iter_gt_queries(gt_root: Path) -> Iterable[Tuple[str, str, Path]]:
    """
    Yields (seq, query, gt_file) from outputs/refer_kitti_motc_gt/*/gt/gt.txt
    Folder name format: <seq>__<query>
    """
    if not gt_root.is_dir():
        return
    for d in sorted([p for p in gt_root.iterdir() if p.is_dir()]):
        name = d.name
        if "__" not in name:
            continue
        seq, query = name.split("__", 1)
        gt_file = d / "gt" / "gt.txt"
        yield seq, query, gt_file


def format_row(cols: Sequence[str], widths: Sequence[int]) -> str:
    out = []
    for c, w in zip(cols, widths):
        out.append(str(c).ljust(w))
    return "  ".join(out)


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument(
        "--gt_root",
        type=str,
        default="outputs/refer_kitti_motc_gt",
        help="Positive labels root (TrackEval-style): outputs/refer_kitti_motc_gt",
    )
    p.add_argument(
        "--ikun_root",
        type=str,
        default="ikun_refermllm_gt/ikun",
        help="ikun results root: contains <seq>/<query>/predict.txt",
    )
    p.add_argument(
        "--refermllm_root",
        type=str,
        default="ikun_refermllm_gt/refermllm",
        help="refermllm results root: contains <seq>/<query>/predict_with_conf.txt",
    )
    p.add_argument(
        "--mot_dir",
        type=str,
        default="",
        help="Directory containing candidate MOT files: <seq>.txt. If empty, auto-pick latest runs/**/probs/mot.",
    )
    p.add_argument("--runs_root", type=str, default="runs", help="Where to search latest mot_dir when --mot_dir is empty.")
    p.add_argument("--refer_threshold", type=float, default=0.5, help="Threshold for refermllm predict_with_conf.")
    p.add_argument("--sequences", type=str, default="0005,0011,0013", help="Optional: comma-separated seq ids (e.g. 0005,0011).")
    p.add_argument("--limit", type=int, default=0, help="Optional: limit number of gt queries evaluated (for quick checks).")
    p.add_argument("--out_json", type=str, default="", help="Optional: write detailed results to JSON.")

    args = p.parse_args()

    root = Path(os.getcwd())
    gt_root = (root / args.gt_root).resolve()
    ikun_root = (root / args.ikun_root).resolve()
    refer_root = (root / args.refermllm_root).resolve()

    seq_filter: Optional[Set[str]] = None
    if str(args.sequences).strip():
        seq_filter = {s.strip() for s in str(args.sequences).split(",") if s.strip()}

    if args.mot_dir:
        mot_dir = Path(args.mot_dir).expanduser().resolve()
    else:
        mot_dir = find_best_mot_dir((root / args.runs_root).resolve(), required_seqs=seq_filter) or Path("")

    if not mot_dir or not mot_dir.is_dir():
        raise FileNotFoundError(
            "找不到候选 MOT 文件目录。请用 --mot_dir 指向 runs/**/probs/mot，"
            "例如：runs/xxxx/eval/xxx/probs/mot"
        )

    # Cache candidates per seq
    candidates_by_seq: Dict[str, List[SampleKey]] = {}

    results: List[Dict[str, object]] = []
    overall_ikun = Confusion()
    overall_refer = Confusion()
    missing_ikun = 0
    missing_refer = 0
    missing_mot = 0
    used = 0

    for (seq, query, gt_file) in iter_gt_queries(gt_root):
        if seq_filter is not None and seq not in seq_filter:
            continue
        if args.limit and used >= int(args.limit):
            break

        mot_file = mot_dir / f"{seq}.txt"
        if seq not in candidates_by_seq:
            candidates_by_seq[seq] = parse_mot_candidates(mot_file) if mot_file.is_file() else []
        candidates = candidates_by_seq[seq]
        if not candidates:
            # no candidates => cannot define universe, skip
            missing_mot += 1
            continue

        pos_by_frame = parse_gt_positive(gt_file)

        ikun_pred_file = ikun_root / seq / query / "predict.txt"
        refer_pred_file = refer_root / seq / query / "predict_with_conf.txt"

        ikun_pred: Optional[Set[SampleKey]] = None
        refer_conf: Optional[Dict[SampleKey, float]] = None

        if ikun_pred_file.is_file():
            ikun_pred = parse_ikun_predict(ikun_pred_file)
        else:
            missing_ikun += 1

        if refer_pred_file.is_file():
            refer_conf = parse_refermllm_predict_with_conf(refer_pred_file)
        else:
            missing_refer += 1

        ikun_c, refer_c = compute_confusion(
            candidates=candidates,
            pos_by_frame=pos_by_frame,
            ikun_pred=ikun_pred,
            refer_conf=refer_conf,
            refer_threshold=float(args.refer_threshold),
        )
        overall_ikun.add(ikun_c)
        overall_refer.add(refer_c)

        r = {
            "seq": seq,
            "query": query,
            "gt_file": str(gt_file),
            "mot_file": str(mot_file),
            "ikun_predict": str(ikun_pred_file),
            "refermllm_predict_with_conf": str(refer_pred_file),
            "refer_threshold": float(args.refer_threshold),
            "ikun": ikun_c.metrics(),
            "refermllm": refer_c.metrics(),
        }
        results.append(r)
        used += 1

    # Print summary (overall + a small per-query table)
    overall = {
        "mot_dir": str(mot_dir),
        "gt_root": str(gt_root),
        "ikun_root": str(ikun_root),
        "refermllm_root": str(refer_root),
        "refer_threshold": float(args.refer_threshold),
        "num_queries": int(len(results)),
        "missing_ikun_queries": int(missing_ikun),
        "missing_refermllm_queries": int(missing_refer),
        "skipped_missing_mot_candidates": int(missing_mot),
        "overall": {
            "ikun": overall_ikun.metrics(),
            "refermllm": overall_refer.metrics(),
        },
    }

    print("=== Overall ===")
    print(json.dumps(overall, ensure_ascii=False, indent=2))

    # Per-query (top N)
    show_n = min(10, len(results))
    if show_n > 0:
        print("\n=== Per-query (first {}) ===".format(show_n))
        headers = [
            "seq__query",
            "ikun_acc",
            "ikun_prec",
            "ikun_rec",
            "refer_acc",
            "refer_prec",
            "refer_rec",
        ]
        widths = [38, 9, 9, 9, 10, 10, 9]
        print(format_row(headers, widths))
        print(format_row(["-" * w for w in widths], widths))
        for r in results[:show_n]:
            key = f'{r["seq"]}__{r["query"]}'
            iku = r["ikun"]
            ref = r["refermllm"]
            row = [
                key[:38],
                f'{float(iku["acc"]):.4f}',
                f'{float(iku["precision"]):.4f}',
                f'{float(iku["recall"]):.4f}',
                f'{float(ref["acc"]):.4f}',
                f'{float(ref["precision"]):.4f}',
                f'{float(ref["recall"]):.4f}',
            ]
            print(format_row(row, widths))

    if args.out_json:
        out_path = Path(args.out_json).expanduser().resolve()
        out_path.parent.mkdir(parents=True, exist_ok=True)
        payload = {"overall": overall, "per_query": results}
        out_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")
        print(f"\nWrote: {out_path}")


if __name__ == "__main__":
    main()

